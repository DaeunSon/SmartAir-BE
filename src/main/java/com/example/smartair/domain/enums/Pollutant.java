package com.example.smartair.domain.enums;

/**
 * 공기 오염 물질의 종류를 나타내는 Enum
 */
public enum Pollutant {
    PM10,   // 미세먼지 (10µm)
    PM25,   // 초미세먼지 (2.5µm)
    ECO2,   // 등가 이산화탄소 (Equivalent CO2)
    CO2,    // 이산화탄소 (Carbon Dioxide)
    TVOC    // 총 휘발성 유기 화합물 (Total Volatile Organic Compounds)
} 